<template>
  <div id="app">
    <!-- 头部start -->
    <div v-if="isShowTop == 1">
			<o-top-bar></o-top-bar>
		</div>
    <!-- 头部end -->
    
    <!-- 侧边栏start -->
    <div v-if="isShowSide == 1">
      <o-side-bar></o-side-bar>
    </div>
    <!-- 侧边栏end -->

   	<!-- 缓存组件,created只触发一次啦 -->
    <keep-alive>
    		<router-view v-if="$route.meta.keepAlive"></router-view>
		</keep-alive>

		<!-- 设置不缓存的页面 -->
		<router-view v-if="!$route.meta.keepAlive"></router-view>

    <!-- 脚步start -->
		<div v-if="isShowFooter == 1">
			<o-footer></o-footer>
		</div>
    <!-- 脚步end -->

    <!-- 聊天 -->
    <o-we-chat></o-we-chat>

  </div>
</template>

<script>
import oTopBar from "./base/topBar/topBar";
import oFooter from "./base/footer/footer";
import oSideBar from "./base/sideBar/sidebar";
import oWeChat from "./base/weChat/weChat";
export default {
  data() {
    return {
      isShowTop:  0, //显示1 隐藏0
      isShowSide: 1, //显示1 隐藏0
      isShowFooter: 0, //显示1 隐藏0
      city: null,
    };
  },
  created() {
    //初始化
    this.$store.commit('FIRSTSTATUS');
    this._mapquerys();
  },
  components: {
    oTopBar,
    oSideBar,
    oFooter,
    oWeChat
  },
  methods:{
    _mapquerys(){
      this.$http.get(this.$url.URL.DEFAULT_CITY)
				.then((response)=>{
          localStorage.selectCity = JSON.stringify(response.data.data);
          this.appinthouse();
				})
    },
    appinthouse() {
      this.$http.get(this.$url.URL.APPOINT_DETAILLIST +"?pageNo="+1,{
					scity: JSON.parse(localStorage.selectCity),//用户选定城市
				})
				.then(response =>{
				let newData = response.data.data;
				//初始化清单列表
				this.$store.commit('CHUSHIHUA', newData);
				})
    }
  },
  watch: {
    $route(to, from) {
      let path = to.path;
      if (path == "/mapSearch") {
        this.isShowTop = 0;
        this.isShowFooter = 0;
        this.isShowSide = 0;
      } 
      else if(path == "/home"||path == "/"){
        this.isShowTop = 0;
        this.isShowSide = 1;   
        this.isShowFooter = 1;
      } 
      else {
        this.isShowTop = 1;     
        this.isShowSide = 1;   
        this.isShowFooter = 1;
      }
    }
  }
};
</script>

